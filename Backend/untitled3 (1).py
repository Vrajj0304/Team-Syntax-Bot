for col in cols_to_fix:
    temp_df[col] = pd.to_numeric(temp_df[col], errors='coerce')
temp_df = temp_df.dropna()

X_temp = temp_df[['YEAR', 'JAN-FEB', 'MAR-MAY', 'JUN-SEP', 'OCT-DEC']]
