X_train_temp, X_test_temp, y_train_temp, y_test_temp = train_test_split(X_temp, y_temp, test_size=0.2, random_state=42)

# 2. Preprocessing sm_gujarat_df
# Reloading to ensure clean state and fixing potential issues with string representation of numbers
sm_gujarat_df = pd.read_csv('/content/sm_Gujarat_2018.csv')
sm_gujarat_df['Average SoilMoisture Volume (at 15cm)'] = sm_gujarat_df['Average SoilMoisture Volume (at 15cm)'].astype(str).str.strip()
sm_gujarat_df['Average SoilMoisture Volume (at 15cm)'] = pd.to_numeric(sm_gujarat_df['Average SoilMoisture Volume (at 15cm)'], errors='coerce')

# If data is still missing, we fill with median instead of dropping to avoid empty datasets
if sm_gujarat_df['Average SoilMoisture Volume (at 15cm)'].isna().all():
    # Fallback if the column is entirely non-numeric
    sm_gujarat_df['Average SoilMoisture Volume (at 15cm)'] = 0.0
else:
    sm_gujarat_df['Average SoilMoisture Volume (at 15cm)'] = sm_gujarat_df['Average SoilMoisture Volume (at 15cm)'].fillna(sm_gujarat_df['Average SoilMoisture Volume (at 15cm)'].median())

le_district = LabelEncoder()
sm_gujarat_df['DistrictName_Encoded'] = le_district.fit_transform(sm_gujarat_df['DistrictName'])

X_sm = sm_gujarat_df[['Average Soilmoisture Level (at 15cm)', 'Aggregate Soilmoisture Percentage (at 15cm)', 'Volume Soilmoisture percentage (at 15cm)', 'DistrictName_Encoded']]
